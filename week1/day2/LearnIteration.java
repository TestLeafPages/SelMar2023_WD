package week1.day2;

public class LearnIteration {

	public static void main(String[] args) {
		
		//for-> ctrl + space--Select 2nd option
		
		//for(int i=0;i<=10;i++)
		//i=1 is starting number
		//i<=10--till condition true
		//i++-increment-one by one
		//1--1<=10--
		//1+1=2--2<=10-........
		//10+1=11--11<=10
		//i++ --posted
		//++i --pre
		
		//10<10
		for(int i=1;i<10;i++) {
		
			System.out.println(i);
		}
		
		System.out.println("------------------------------------------");

		
		for (int i = 10; i>=1; i--) {

			System.out.println(i);
		}
	}

}
